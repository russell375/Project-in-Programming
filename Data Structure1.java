public class Datastructure1 {
    public static void main(String[] args) {

        //Array String
        String[] names = {"Kim Endro", "Razel", "Janecres", "Dahulo", "Goryo", "Cardo"};

        //Array Integer
        int[] age = {18, 19, 23, 35, 41, 46};

        //Array Boolean
        boolean[] student = {true, true, true, false, false, false};

        //Print out String, Int, and Boolean
        System.out.println(names[0]);
        System.out.println(age[0]);
        System.out.println(student[0]);

    }
}
